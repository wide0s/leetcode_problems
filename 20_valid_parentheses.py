class Solution(object):
    def isValid(self, s):
        """
        :type s: str
        :rtype: bool
        """
        if len(s) % 2 != 0:
            return False
        twins = { '}': '{', ']': '[', ')' : '('}
        expression = []
        for ch in s:
            if len(expression) > 0 and (ch in twins and expression[-1] == twins[ch]):
                expression.pop()
            else:
                expression.append(ch)
        return True if len(expression) == 0 else False

